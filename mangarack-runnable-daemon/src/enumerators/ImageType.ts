'use strict';

/**
 * Represents an image type.
 */
export enum ImageType {
  /**
   * Represents an unknown image type.
   */
  Unknown,

  /**
   * Represents an image in the gif format.
   */
  Gif,

  /**
   * Represents an image in the jpg format.
   */
  Jpg,

  /**
   * Represents an image in the png format.
   */
  Png
}
