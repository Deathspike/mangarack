/**
 * Represents a modal type.
 */
export enum ModalType {
  /**
   * Represents no modal.
   */
  None,

  /**
   * Represents a download modal.
   */
  Download,

  /**
   * Represents a delete modal.
   */
  Delete,

  /**
   * Represents an error modal.
   */
  Error,

  /**
   * Represents a pending modal.
   */
  Pending,

  /**
   * Represents a series modal.
   */
  Series
}
