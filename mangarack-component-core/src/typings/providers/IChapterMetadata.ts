/**
 * Represents chapter metadata.
 */
export interface IChapterMetadata {
  /**
   * Contains the number.
   */
  number?: number;

  /**
   * Contains the title.
   */
  title: string;

  /**
   * Contains the version.
   */
  version?: number;

  /**
   * Contains the volume.
   */
  volume?: number;
}
