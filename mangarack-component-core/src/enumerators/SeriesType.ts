/**
 * Represents a series type.
 */
export enum SeriesType {
  /**
   * Represents an art book.
   */
  Artbook = 1,

  /**
   * Represents a Japanese manga.
   */
  Manga,

  /**
   * Represents a Korean Manhwa.
   */
  Manhua,

  /**
   * Represents a Chinese Manwha.
   */
  Manhwa,

  /**
   * Represents an other series type.
   */
  Other
}
